// Pure resolver for the namespaced local `.env` (design
// docs/superpowers/specs/2026-06-23-supabase-env-namespacing-design.md).
//
// `.env` holds both Supabase projects under SUPABASE_STAGE_* / SUPABASE_PROD_*
// plus a SUPABASE_TARGET switch (default `stage`). This maps the selected
// target's block onto the *canonical* names the app and the gated tests read
// (SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY, SUPABASE_ANON_KEY,
// SUPABASE_STORAGE_BUCKET, and the browser-inlined NEXT_PUBLIC_SUPABASE_*).
//
// It lives in scripts/ (not src/) so the dependency-rule guard — only
// src/composition/ may read process.env — stays satisfied. The function is pure:
// it takes an env object and returns a result, so it is unit-testable and the
// callers (the .env.local writer and the vitest.supabase config) own the I/O.

const TARGETS = ["stage", "prod"];

/**
 * @param {Record<string, string | undefined>} env
 * @returns {{ target: string, canonical: Record<string, string> | null }}
 *   `canonical` is null when the selected target has no URL configured (keyless
 *   / CI / unconfigured) — callers treat that as a no-op rather than an error.
 */
export function resolveSupabaseEnv(env) {
  const target = (env.SUPABASE_TARGET ?? "stage").toLowerCase();
  if (!TARGETS.includes(target)) {
    throw new Error(
      `Invalid SUPABASE_TARGET="${env.SUPABASE_TARGET}". Expected one of: ${TARGETS.join(", ")}.`,
    );
  }

  const prefix = `SUPABASE_${target.toUpperCase()}_`;
  const url = env[`${prefix}URL`];
  const serviceRoleKey = env[`${prefix}SERVICE_ROLE_KEY`];
  const anonKey = env[`${prefix}ANON_KEY`];
  const bucket = env[`${prefix}STORAGE_BUCKET`];

  // No URL for the selected target => nothing to resolve (keyless / CI). Return
  // null instead of throwing so wiring the resolver into `npm run build` never
  // breaks the keyless CI build.
  if (!url) {
    return { target, canonical: null };
  }

  const missing = [];
  if (!serviceRoleKey) missing.push(`${prefix}SERVICE_ROLE_KEY`);
  if (!anonKey) missing.push(`${prefix}ANON_KEY`);
  if (missing.length > 0) {
    throw new Error(
      `Supabase target "${target}" is partially configured — missing: ${missing.join(", ")}.`,
    );
  }

  return {
    target,
    canonical: {
      SUPABASE_URL: url,
      SUPABASE_SERVICE_ROLE_KEY: serviceRoleKey,
      SUPABASE_ANON_KEY: anonKey,
      SUPABASE_STORAGE_BUCKET: bucket || "chore-photos",
      NEXT_PUBLIC_SUPABASE_URL: url,
      NEXT_PUBLIC_SUPABASE_ANON_KEY: anonKey,
    },
  };
}

/**
 * Render the canonical mapping as the body of a generated `.env.local`. The
 * banner warns humans off editing it (it is regenerated on every dev/build/start)
 * and records which Supabase target is active.
 *
 * @param {string} target
 * @param {Record<string, string>} canonical
 * @returns {string}
 */
export function formatEnvLocal(target, canonical) {
  const banner = [
    "# GENERATED by scripts/write-supabase-env-local.mjs — do not edit.",
    "# Regenerated on every npm run dev/build/start.",
    `# Local Supabase target: ${target.toUpperCase()}`,
  ];
  const lines = Object.entries(canonical).map(([key, value]) => `${key}=${value}`);
  return `${[...banner, ...lines].join("\n")}\n`;
}
